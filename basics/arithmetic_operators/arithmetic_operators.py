# ==========================================
# Python Arithmetic Operators
# ==========================================

# Variables
a = 20
b = 6

print("First Number :", a)
print("Second Number:", b)

# ------------------------------------------
# Addition (+)
# ------------------------------------------
print("\n===== Addition (+) =====")

addition = a + b

print(f"{a} + {b} = {addition}")

# ------------------------------------------
# Subtraction (-)
# ------------------------------------------
print("\n===== Subtraction (-) =====")

subtraction = a - b

print(f"{a} - {b} = {subtraction}")

# ------------------------------------------
# Multiplication (*)
# ------------------------------------------
print("\n===== Multiplication (*) =====")

multiplication = a * b

print(f"{a} * {b} = {multiplication}")

# ------------------------------------------
# Division (/)
# ------------------------------------------
print("\n===== Division (/) =====")

division = a / b

print(f"{a} / {b} = {division}")

# ------------------------------------------
# Modulus (%)
# ------------------------------------------
print("\n===== Modulus (%) =====")

modulus = a % b

print(f"{a} % {b} = {modulus}")

# ------------------------------------------
# Exponentiation (**)
# ------------------------------------------
print("\n===== Exponentiation (**) =====")

power = a ** b

print(f"{a} ** {b} = {power}")

# ------------------------------------------
# Floor Division (//)
# ------------------------------------------
print("\n===== Floor Division (//) =====")

floor_division = a // b

print(f"{a} // {b} = {floor_division}")

# ------------------------------------------
# Example with Float Numbers
# ------------------------------------------
print("\n===== Float Example =====")

x = 10.5
y = 3

print(f"{x} + {y} = {x + y}")
print(f"{x} - {y} = {x - y}")
print(f"{x} * {y} = {x * y}")
print(f"{x} / {y} = {x / y}")
print(f"{x} // {y} = {x // y}")
print(f"{x} % {y} = {x % y}")
print(f"{x} ** {y} = {x ** y}")

# ------------------------------------------
# End of Program
# ------------------------------------------
print("\nProgram finished.")